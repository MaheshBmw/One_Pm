package flipkart.com;

public class For_Loop {
	
	public static void main(String[] args) {
		
		switch ("D") {
		case "A":
			System.out.println("AARTHI");
			break;
        case "B":
			System.out.println("SHRUTI");
			break;
        case "C":
			System.out.println("KARTHI");
			break;
		default:
			System.out.println("INVALID");
			break;
		}

}
}